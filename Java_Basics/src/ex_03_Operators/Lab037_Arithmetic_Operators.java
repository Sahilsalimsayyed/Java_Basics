package ex_03_Operators;

public class Lab037_Arithmetic_Operators {

	public static void main(String[] args) {
//		Arithmetic Operators
//		        + (Adition)
//		        - (Subtraction)
//		        * (Multiplication)
//		        / (Division)
//		        % (Modulus) | Modulus returns - Reminder
		
		int a = 20;
		int b = 3;
		float c = 3.0f;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a/c);
		
		System.out.println("a+b"); // String format
		System.out.println(a+b);


	}
}
