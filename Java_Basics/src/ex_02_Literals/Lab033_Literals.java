package ex_02_Literals;

public class Lab033_Literals {

	public static void main(String[] args) {
		
		int age = 65; //How 65 value will store in data storage
		
		//int age = 65; (int -> 32Bits)
		//32 Bits -> 00000000000000000000000001000001
		
		//we can store also binary value
		int binary_num = 0b1010; // Decimal System - 10;
		
		//Octal base? -> 8;
		int octal = 0101;
		
		//Hexadecimal Literals - 16;
		int hex = 0xface;
		//base 16 color combination are in hex - #28a745
	}
}
